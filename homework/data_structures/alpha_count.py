# 你的朋友终于解决了键盘————换了块好的，
# 你现在突然好奇平时正常输入时，不同字母又会在一句话中占多少个
# 输入：一句话，可能是任何语言
# 输出：字母和计数，格式任意；只输出出现两次以上的字。

# 注意：字母一律视为小写形式来计数
# 注意： str.isalpha() 可以检查是否为字母（这个字母是广义的，汉字也是字母）

# 提示: for ch in s 可以依次取出字符。

# 示例（不作为格式要求，不要求输出的顺序。）
# 输入：The quick brown fox jumps over the lazy dog
# 输出：
# t: 2
# h: 2
# e: 3
# u: 2
# r: 2
# o: 4

# 输入：一句话就是一个完整的想法。一段话是一系列紧密相关的想法。
# 输出：
# 一: 4
# 话: 2
# 是: 2
# 的: 2
# 想: 2
# 法: 2
